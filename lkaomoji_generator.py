def random_face() -> str:
    left_arm, right_arm  = random.choice(ARMS)
    left_eye, right_eye  = random.choice(EYES)
    mouth                = random.choice(MOUTHS)
    # Stitch them together in the requested order
    return f"{left_arm}{left_eye}{mouth}{right_eye}{right_arm}"

